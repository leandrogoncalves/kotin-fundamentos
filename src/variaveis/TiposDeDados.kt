package variaveis

fun main() {
    //Tipos de dados n√∫meriocos:
    /**
     * Byte: 8 bits
     * Short: 16 bits
     * Int: 32 bits
     * Long: 64 bits
     * Float: 128 bits
     * Double: 256 bits
     */
    val byte: Byte = 8
    val short: Short = 360
    val int: Int = 1000
    val long: Long = 1000000000000000000L
    val float: Float = 10.0f
    val double: Double = 10.0

    println("byte: $byte")
    println("short: $short")
    println("int: $int")
    println("long: $long")
    println("float: $float")
    println("double: $double")

    // Tipo booleano
    /**
     * boolean: true ou false
     */
    val boolean: Boolean = false
    println("boolean: $boolean")

    // Tipos textuais
    /**
     * char
     */
    var char: Char = 'c'
    val string: String = "String"

    println("char: $char")
    println("string: $string")


}